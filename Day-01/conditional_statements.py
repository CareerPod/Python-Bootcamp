# IF Statement

age = 20
if age >= 18:
    print("You are eligible to vote.")

# IF else Statement
age = 16
if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote.")

#if else elif statement
age = 25

if age < 18:
    print("You are a minor.")
elif 18 <= age < 60:
    print("You are an adult.")
else:
    print("You are a senior citizen.")
