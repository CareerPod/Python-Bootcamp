# 1. Print Function
print("Hello, World!")

# 2. Input Function
name = input("Enter your name: ")
print("Hello,", name)

# 3. Length of a String or List
my_list = [1, 2, 3, 4, 5]
print("Length of list:", len(my_list))

# 4. Sum of Numbers
numbers = [10, 20, 30, 40]
print("Sum of numbers:", sum(numbers))

# 5. Maximum and Minimum
print("Max value:", max(numbers))
print("Min value:", min(numbers))

# 6. Range Function
for i in range(1, 6):  # From 1 to 5
    print("Number:", i)

# 7. Type Function
print("Type of list:", type(my_list))

# 8. Sorted Function
unsorted_list = [3, 1, 4, 2]
print("Sorted list:", sorted(unsorted_list))
